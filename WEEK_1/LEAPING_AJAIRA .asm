CODE SEGMENT 
 ASSUME CS:CODE, DS:CODE 
 
 MOV AX,90H     ;INPUT X
 MOV BX,10H     ;INPUT Y
 
 CMP AX,1000H    
 JG SUMMATION   ;IS X>1000?
 
 CMP BX,1000H
 JL SUBTRACTION;IS Y<1000?
 
SUMMATION:
 CMP BX,100H
 JL BOTH_OK     ; x>1000 AND Y<100 
 ADD AX,BX
 JMP LAST
 
SUBTRACTION:
 SUB AX,BX
 JMP LAST 
 
BOTH_OK:
 NOT AX
 JMP LAST 
 
LAST: 
 HLT
CODE ENDS 
 END 
 
 
 
 